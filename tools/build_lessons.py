         "    dist = {start: 0}; q = deque([start])",
         "    while q:",
         "        u = q.popleft()",
         "        for v in adj[u]:",
         "            if v not in dist: dist[v] = dist[u] + 1; q.append(v)",
         "    return dist",
         "",
